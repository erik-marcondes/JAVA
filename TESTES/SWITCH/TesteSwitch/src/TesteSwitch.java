
public class TesteSwitch {

	public static void main(String[] args) {
		int posicao = 5;
		switch (posicao){
		case 1:
			System.out.println("Primeiro");
			break;
		case 2:
			System.out.println("Segundo");
			break;
		case 3:
			System.out.println("Terceiro");
			break;
		default:
			System.out.println("Não subiu no pódio!");
			break;
		}
	}

}
